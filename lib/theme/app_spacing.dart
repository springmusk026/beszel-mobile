/// Design tokens for spacing used throughout the Beszel application.
/// Provides consistent spacing values for margins, padding, and gaps.
abstract class AppSpacing {
  /// Extra small spacing: 4.0
  static const double xs = 4.0;

  /// Small spacing: 8.0
  static const double sm = 8.0;

  /// Medium spacing: 12.0
  static const double md = 12.0;

  /// Large spacing: 16.0
  static const double lg = 16.0;

  /// Extra large spacing: 24.0
  static const double xl = 24.0;

  /// Extra extra large spacing: 32.0
  static const double xxl = 32.0;

  /// Card internal padding
  static const double cardPadding = lg;

  /// Card element spacing
  static const double cardElementSpacing = md;

  /// List item vertical spacing
  static const double listItemVertical = 6.0;

  /// Screen horizontal padding
  static const double screenHorizontal = lg;

  /// Screen vertical padding
  static const double screenVertical = lg;
}
